package com.ims.report.client.exceptions;

/**
 * @author vpurdila
 *
 */
public class HttpReportClientException extends Exception
{
	public HttpReportClientException()
	{
		super();
	}

	public HttpReportClientException(String arg0)
	{
		super(arg0);
	}

	public HttpReportClientException(Throwable arg0)
	{
		super(arg0);
	}

	public HttpReportClientException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}
}
